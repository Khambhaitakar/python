#  Write a Python program to demonstrate the use of functions from the math module.
import math

# Number for demonstration
num = 25

# Using math functions
print("Square root of", num, ":", math.sqrt(num))
print("Factorial of", num, ":", math.factorial(num))
print("Ceiling of 4.7:", math.ceil(4.7))
print("Floor of 4.7:", math.floor(4.7))
print("Value of Pi:", math.pi)
print("Exponential of 2:", math.exp(2))
print("Logarithm of", num, "to base 10:", math.log10(num))
